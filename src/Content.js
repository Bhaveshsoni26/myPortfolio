const getCountForExperience = () => {
  const today = new Date();
  const start = new Date("2023-01-04");
  const diff = today - start;
  let time = Math.floor(diff / (1000 * 60 * 60 * 24 * 24));
  let p = "Months";

  if(time > 12){
    time = Math.floor(time / 12);
    p = "Years";
  }
  return {count: time , period: p};
};
